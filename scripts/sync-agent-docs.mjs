#!/usr/bin/env node
/**
 * Generate agent docs derived from code (avoid hand-maintained inventories).
 * Run from repo root: node scripts/sync-agent-docs.mjs [--check]
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const bundledDataTs = path.join(repoRoot, "app/src/core/bundledData.ts");
const appSrc = path.join(repoRoot, "app/src");
const outFile = path.join(repoRoot, "docs/agent/generated/bundled-data-catalog.md");

/** Extra context appended to the loader cell (not a second path). */
const FILE_NOTES = {
  "gamedata.json": "bundled-only via `app/src/main/gameDataProvider.ts` (fail-fast at startup)",
};

function parseRequiredFiles() {
  const src = fs.readFileSync(bundledDataTs, "utf8");
  const m = src.match(/REQUIRED_BUNDLED_DATA_FILES\s*=\s*\[([\s\S]*?)\]\s*as const/);
  if (!m) throw new Error("Could not parse REQUIRED_BUNDLED_DATA_FILES in bundledData.ts");
  const files = [...m[1].matchAll(/"([^"]+\.json)"/g)].map((x) => x[1]);
  if (files.length === 0) throw new Error("REQUIRED_BUNDLED_DATA_FILES is empty");
  return files;
}

function walkTsFiles(dir, out = []) {
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walkTsFiles(full, out);
    else if (entry.name.endsWith(".ts") || entry.name.endsWith(".tsx")) out.push(full);
  }
  return out;
}

function relativeLoader(filePath) {
  const rel = path.relative(path.join(repoRoot, "app/src"), filePath).replace(/\\/g, "/");
  return `\`app/src/${rel}\``;
}

function scanLoaders(requiredFiles) {
  const byFile = new Map(requiredFiles.map((f) => [f, new Set()]));

  for (const filePath of walkTsFiles(appSrc)) {
    if (filePath.endsWith("bundledData.ts")) continue;
    const content = fs.readFileSync(filePath, "utf8");
    const rel = relativeLoader(filePath);

    for (const match of content.matchAll(/readBundledJson(?:<[^>]*>)?\(\s*"([^"]+\.json)"/g)) {
      const json = match[1];
      if (byFile.has(json)) byFile.get(json).add(rel);
    }
    for (const match of content.matchAll(/bundledDataPath\(\s*"([^"]+\.json)"/g)) {
      const json = match[1];
      if (byFile.has(json)) byFile.get(json).add(rel);
    }
  }

  return byFile;
}

function formatCatalog(requiredFiles, loadersByFile) {
  const lines = [
    "# Bundled data catalog (generated)",
    "",
    "Do not edit by hand. Source of truth: `app/src/core/bundledData.ts` → `REQUIRED_BUNDLED_DATA_FILES`.",
    "",
    "Regenerate from repo root:",
    "",
    "```powershell",
    "pnpm run sync:agent-docs",
    "```",
    "",
    `Last generated by \`scripts/sync-agent-docs.mjs\` (commit the diff when loaders change).`,
    "",
    "| File | Loaded by |",
    "|------|-----------|",
  ];

  for (const file of requiredFiles) {
    const loaders = loadersByFile.get(file);
    let cell =
      loaders && loaders.size > 0
        ? [...loaders].sort().join("; ")
        : "_no `readBundledJson` / `bundledDataPath` found — update script or loaders_";
    const note = FILE_NOTES[file];
    if (note) cell += ` (${note})`;
    lines.push(`| \`${file}\` | ${cell} |`);
  }

  lines.push("");
  return lines.join("\n");
}

const checkOnly = process.argv.includes("--check");
const requiredFiles = parseRequiredFiles();
const loadersByFile = scanLoaders(requiredFiles);
const content = formatCatalog(requiredFiles, loadersByFile);

fs.mkdirSync(path.dirname(outFile), { recursive: true });

if (checkOnly) {
  if (!fs.existsSync(outFile)) {
    console.error(`Missing ${path.relative(repoRoot, outFile)}. Run: pnpm run sync:agent-docs`);
    process.exit(1);
  }
  const existing = fs.readFileSync(outFile, "utf8");
  if (existing !== content) {
    console.error(
      `${path.relative(repoRoot, outFile)} is out of sync with code. Run: pnpm run sync:agent-docs`,
    );
    process.exit(1);
  }
  console.log("Agent docs mirror OK (bundled-data-catalog).");
  process.exit(0);
}

fs.writeFileSync(outFile, content, "utf8");
console.log(`wrote ${path.relative(repoRoot, outFile)} (${requiredFiles.length} catalogs)`);
